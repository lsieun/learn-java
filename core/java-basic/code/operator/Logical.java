public class Logical {

    public void add() {
        boolean a = true;
        boolean b = false;
        boolean result = (a && b);
    }

    public void or() {
        boolean a = true;
        boolean b = false;
        boolean result = (a || b);
    }

    public void not() {
        boolean a = true;
        boolean result = !a;
    }
}
