package lsieun.number.range;

public class B_ShortRange {
    public static void main(String[] args) {
        short minValue = Short.MIN_VALUE;// -32768
        short maxValue = Short.MAX_VALUE;// 32767

        System.out.println("Short Size: " + Short.SIZE);
        System.out.println("Short Bytes: " + Short.BYTES);
        System.out.println("Short Min Value: " + minValue);
        System.out.println("Short Max Value: " + maxValue);
    }
}
