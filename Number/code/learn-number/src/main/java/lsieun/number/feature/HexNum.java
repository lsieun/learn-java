package lsieun.number.feature;

public class HexNum {
    public static void main(String[] args) {
        System.out.println("0x00: " + 0x00); // 0
        System.out.println("0x01: " + 0x01); // 1
        System.out.println("0x09: " + 0x09); // 9
        System.out.println("0x0a: " + 0x0a); // 10
        System.out.println("0x0b: " + 0x0b); // 11
        System.out.println("0x0c: " + 0x0c); // 12
        System.out.println("0x0d: " + 0x0d); // 13
        System.out.println("0x0e: " + 0x0e); // 14
        System.out.println("0x0f: " + 0x0f); // 15
        System.out.println("0x10: " + 0x10); // 16
        System.out.println("0xff: " + 0xff); // 255
    }
}
